package org.auctionsproject.ui;

/**
 * Separater Einstiegspunkt, der die JavaFX-Anwendung startet.
 */
public class Launcher {
    /**
     * Startet die Anwendung und delegiert an {@link App#main(String[])}.
     *
     * @param args Kommandozeilenargumente.
     */
    public static void main(String[] args) {
        // Dies ruft die main-Methode deiner App-Klasse auf
        App.main(args);
    }
}
